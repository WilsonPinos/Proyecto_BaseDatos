    private void txtcodigoKeyTyped(java.awt.event.KeyEvent evt) {
        char letra = evt.getKeyChar();
        if (!Character.isDigit(letra)) {
            evt.consume();
        }

        if (txtcodigo.getText().length() > 14) {
            evt.consume();
        }
    }

