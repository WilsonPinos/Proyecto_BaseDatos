    public void RecuperarDato() {
        ObjectContainer db = null;

        try {
            // Abrir la base de datos (puedes cambiar el nombre del archivo.db4o seg√∫n tus necesidades)
            db = Db4o.openFile(BDdireccion);

            Conductor con = new Conductor();
            con.setId_conductor(id_conductor.getText());

            Bus bus1 = new Bus();
            bus1.setMatricula(cbxbus.getSelectedItem().toString());

            Historial_asignacion his = new Historial_asignacion();
            his.setCondu(con);
            his.setBu(bus1);

            // Guardar los objetos Padre e Hijo en la base de datos
            db.store(bus1);
            db.store(con);
            db.store(his);

            Query query = db.query();
            query.constrain(Conductor.class);
            ObjectSet<Conductor> result = query.execute();

            StringBuilder id_conductor = new StringBuilder();
            while (result.hasNext()) {
                Conductor conductorRecuperado = result.next();
                id_conductor.append("Conductor: ").append(conductorRecuperado.getId_conductor()).append("\n");
                id_conductor.append("Asignaciones:\n");

                Query queryAsignaciones = db.query();
                queryAsignaciones.constrain(Historial_asignacion.class);
                queryAsignaciones.descend("condu").constrain(conductorRecuperado).identity();
                ObjectSet<Historial_asignacion> asignaciones = queryAsignaciones.execute();

                while (asignaciones.hasNext()) {
                    Historial_asignacion historia = asignaciones.next();
                    id_conductor.append("- Bus: ").append(historia.getBu().getMatricula()).append("\n");
                }

                id_conductor.append("-----------------------\n");
            }

           // txtResultado.setText(resultadoText.toString());

            db.commit();
        } finally {
            if (db != null) {
                db.close();
            }
        }
    }

