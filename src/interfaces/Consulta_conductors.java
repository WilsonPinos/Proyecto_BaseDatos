      public void BuscarConductor() {
//String Id_cliente, String cedula_cli, String nombre_cli, String apellido_cli, String telefono_cli, String correo_cli, int edad_cli, Date fecha_nacimiento) {

        ObjectContainer base = Db4o.openFile(proyecto_basedatos.BDdireccion);
        Conductor cli = new Conductor(null, null, null, null, null, null, null, null, 0, null);
        ObjectSet resultado = base.get(cli);
        String matriz[][] = new String[resultado.size()][10];
        int i = 0;
        while (resultado.hasNext()) {
            Conductor cond = (Conductor) resultado.next();
            matriz[i][0] = cond.getId_conductor();
            matriz[i][1] = cond.getCedula();
            matriz[i][2] = cond.getAnio_experiencia();
            matriz[i][3] = cond.getTipo_Licencia();
            matriz[i][4] = cond.getId_licencia();
            matriz[i][5] = cond.getNombre();
            matriz[i][6] = cond.getApellido();
            matriz[i][7] = cond.getTelefono();
            int auxedad = cond.getEdad();
            String edad = Integer.toString(auxedad);
            matriz[i][8] = edad;

            Date auxnacim = cond.getFecha_nacimiento();
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
            String nacim = formatoFecha.format(auxnacim);
            matriz[i][9] = nacim;

            i++;

        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(matriz, new String[]{"id de conductor", "cedula", "experiencia", "tipo_Licencia", "id de licencia", "nombre", "apellido", "telefono", "edad", "fecha de nacimiento"
        }));
        CerrarBD(base);
    }

