    private void btnBuscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar2ActionPerformed
        ObjectContainer base = Db4o.openFile(proyecto_basedatos.BDdireccion);
        Conductor client = new Conductor(auxcod, null, null, null, null,null,null, null, 0, null);
        ObjectSet resultado = base.get(client);
        while (resultado.hasNext()) {
            Conductor cliente1 = (Conductor) resultado.next();
            if (JOptionPane.showConfirmDialog(this, "¿Deseas continuar?", "Confirmación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                if (auxcod.equals(cliente1.getId_conductor())) {
                    base.delete(cliente1);

                }
            }
        }
        CerrarBD(base);
        BuscarConductor();
    }//GEN-LAST:event_btnBuscar2ActionPerformed

